export const linearFunction = (x: number): number => {
  return 2 * x + 3; // Example: f(x) = 2x + 3
};

export const quadraticFunction = (x: number): number => {
  return x * x; // Example: f(x) = x^2
};

export const cubicFunction = (x: number): number => {
  return x * x * x; // Example: f(x) = x^3
};

export const constantFunction = (x: number): number => {
  return 5; // Example: f(x) = 5
};
